<p>A Balatonon a római időktől kezdve úsznak hajók, melyeket sokáig leginkább halászatra használtak. 
A balatoni hajózás első nagy korszaka a Festetics családhoz fűződik.  
A általuk üzemeltetett Kristóf nevű „sóhajó” teherszállító vitorlás volt. 
1753-tól szállított sót Balatonkeneséről Keszthelyre és visszafelé sem járt üresen. 
Fedélzetén fával, borral és egyéb áruval megrakva kötött ki Kenesén.</p>
